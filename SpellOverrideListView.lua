local frame
local scroll
local content
local rows = {}        -- pre-created Frames, reused across renders
local statusFS
local _exportPopup

-- ============================================================
-- Geometry persistence
-- ============================================================

local function SavePosition(self)
    local db = GetDB()
    local point, _, relPoint, x, y = self:GetPoint(1)
    db.point, db.relPoint, db.x, db.y = point, relPoint, x, y
end

local function SaveSize(self)
    local db = GetDB()
    db.w, db.h = self:GetWidth(), self:GetHeight()
end

local function ApplySavedGeometry(self)
    local db = GetDB()
    local screenW = UIParent:GetWidth()
    local screenH = UIParent:GetHeight()

    local w = db.w or DEFAULT_W
    local h = db.h or DEFAULT_H
    if w > screenW - 2 * SCREEN_MARGIN then w = screenW - 2 * SCREEN_MARGIN end
    if h > screenH - 2 * SCREEN_MARGIN then h = screenH - 2 * SCREEN_MARGIN end
    if w < MIN_W then w = MIN_W end
    if h < MIN_H then h = MIN_H end

    self:ClearAllPoints()
    if db.point and db.relPoint and db.x and db.y then
        self:SetPoint(db.point, UIParent, db.relPoint, db.x, db.y)
    else
        self:SetPoint("CENTER")
    end
    self:SetSize(w, h)
end

-- ============================================================
-- Row pool — build once, reuse via SetText
-- ============================================================

local function BuildRowPool(parent, totalRows)
    for r = 1, totalRows do
        local row = CreateFrame("Frame", nil, parent)
        row:SetSize(CONTENT_W, ROW_H)
        row:SetPoint("TOPLEFT", parent, "TOPLEFT", 0, -(r - 1) * ROW_H)
        if r % 2 == 0 then
            local bg = row:CreateTexture(nil, "BACKGROUND")
            bg:SetAllPoints()
            bg:SetColorTexture(0.08, 0.08, 0.08, 0.5)
        end
        row.fs = {}
        local x = 0
        for c, def in ipairs(COL) do
            local fs = row:CreateFontString(nil, "OVERLAY", "GameFontNormalSmall")
            fs:SetPoint("LEFT", row, "LEFT", x + 4, 0)
            fs:SetWidth(def.w - 8)
            fs:SetHeight(ROW_H)
            fs:SetJustifyH("LEFT")
            fs:SetJustifyV("MIDDLE")
            fs:SetText("")
            row.fs[c] = fs
            x = x + def.w
        end
        rows[r] = row
    end
end

-- ============================================================
-- Render — sync rows to current snapshot
-- ============================================================

local function Render()
    local entries = GetSortedSnapshot()
    -- Grow pool if needed
    if #entries > #rows and content then
        BuildRowPool(content, #entries)
    end
    -- Update visible content height so the scrollbar maxes correctly
    if content then
        content:SetHeight(math.max(1, #rows * ROW_H))
    end

    for r = 1, #rows do
        local row = rows[r]
        local e = entries[r]
        if e then
            row.fs[1]:SetText(e.class)
            row.fs[2]:SetText(e.spec)
            row.fs[3]:SetText(e.overrideName)
            row.fs[4]:SetText(tostring(e.overrideID))
            row.fs[5]:SetText(e.baseName)
            row.fs[6]:SetText(tostring(e.base))
            -- Color-code source: manual = yellow, auto = grey
            if e.source == "manual" then
                row.fs[7]:SetText("|cFFFFD200manual|r")
            else
                row.fs[7]:SetText("|cFF888888auto|r")
            end
            row:Show()
        else
            for c = 1, #COL do row.fs[c]:SetText("") end
        end
    end

    if statusFS then
        local autoCount, manualCount = 0, 0
        for _, e in ipairs(entries) do
            if e.source == "manual" then manualCount = manualCount + 1
            else autoCount = autoCount + 1 end
        end
        statusFS:SetText(string.format(
            "Total: %d  (manual: %d, auto: %d)",
            #entries, manualCount, autoCount))
    end
end

