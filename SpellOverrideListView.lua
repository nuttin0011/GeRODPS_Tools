-- Export popup
-- ============================================================

local function ShowExportPopup()
    if _exportPopup and _exportPopup:IsShown() then
        _exportPopup:Raise()
        return
    end

    local entries = GetSortedSnapshot()
    local text = BuildExportText(entries)

    if not _exportPopup then
        local f = CreateFrame("Frame", "GeRODPS_ToolsSpellOverrideExportFrame",
            UIParent, "BasicFrameTemplateWithInset")
        _exportPopup = f
        f:SetSize(720, 520)
        f:SetPoint("CENTER")
        f:SetFrameStrata("FULLSCREEN_DIALOG")
        f:SetToplevel(true)
        f:SetMovable(true)
        f:EnableMouse(true)
        f:SetClampedToScreen(true)
        f:RegisterForDrag("LeftButton")
        f:SetScript("OnDragStart", f.StartMoving)
        f:SetScript("OnDragStop",  f.StopMovingOrSizing)
        if f.TitleText then
            f.TitleText:SetText("Spell Override Cache — Export")
        end
        tinsert(UISpecialFrames, "GeRODPS_ToolsSpellOverrideExportFrame")

        local hint = f:CreateFontString(nil, "OVERLAY", "GameFontDisableSmall")
        hint:SetPoint("TOPLEFT", f, "TOPLEFT", 12, -30)
        hint:SetText("Ctrl+A then Ctrl+C to copy.  Paste into SpellOverrideList.lua.")

        local sf = CreateFrame("ScrollFrame", "$parentScroll",
            f, "UIPanelScrollFrameTemplate")
        sf:SetPoint("TOPLEFT",     f, "TOPLEFT",     12, -50)
        sf:SetPoint("BOTTOMRIGHT", f, "BOTTOMRIGHT", -32, 12)

        local eb = CreateFrame("EditBox", nil, sf)
        eb:SetMultiLine(true)
        eb:SetAutoFocus(false)
        eb:SetFontObject(ChatFontNormal)
        eb:SetWidth(sf:GetWidth())
        eb:SetMaxLetters(0)
        eb:SetCountInvisibleLetters(false)
        eb:EnableMouse(true)
        eb:SetScript("OnEscapePressed", function(self) self:ClearFocus() end)
        eb:SetScript("OnCursorChanged", function(self, _, y, _, ch)
            ScrollingEdit_OnCursorChanged(self, 0, y, 0, ch)
        end)
        sf:SetScrollChild(eb)
        f.editBox = eb
    end

    _exportPopup.editBox:SetText(text)
    _exportPopup:Show()
    _exportPopup:Raise()
end

-- ============================================================
-- Frame builder
-- ============================================================

local function CreateFrameOnce()
    if frame then return frame end

    frame = CreateFrame("Frame", FRAME_NAME, UIParent, "BasicFrameTemplateWithInset")
    frame:SetFrameStrata("HIGH")
    frame:SetToplevel(true)
    frame:SetClampedToScreen(true)
    frame:SetMovable(true)
    frame:EnableMouse(true)
    frame:SetUserPlaced(true)
    frame:RegisterForDrag("LeftButton")
    frame:SetScript("OnDragStart", frame.StartMoving)
    frame:SetScript("OnDragStop", function(self)
        self:StopMovingOrSizing()
        SavePosition(self)
    end)
    frame:SetResizable(true)
    if frame.SetResizeBounds then
        frame:SetResizeBounds(MIN_W, MIN_H, MAX_W, MAX_H)
    elseif frame.SetMinResize then
        frame:SetMinResize(MIN_W, MIN_H)
        frame:SetMaxResize(MAX_W, MAX_H)
    end

    if frame.TitleText then
        frame.TitleText:SetText("GeRODPS Tools — Spell Override Cache")
    end

    local inset = frame.Inset or frame

    -- ── Toolbar ──────────────────────────────────────────
    local toolbar = CreateFrame("Frame", nil, inset)
    toolbar:SetHeight(28)
    toolbar:SetPoint("TOPLEFT", inset, "TOPLEFT", 8, -4)
    toolbar:SetPoint("TOPRIGHT", inset, "TOPRIGHT", -8, -4)

    local refreshBtn = CreateFrame("Button", nil, toolbar, "UIPanelButtonTemplate")
    refreshBtn:SetSize(90, 22)
    refreshBtn:SetPoint("LEFT", toolbar, "LEFT", 0, 0)
    refreshBtn:SetText("Refresh")
    refreshBtn:SetScript("OnClick", Render)

    local exportBtn = CreateFrame("Button", nil, toolbar, "UIPanelButtonTemplate")
    exportBtn:SetSize(90, 22)
    exportBtn:SetPoint("LEFT", refreshBtn, "RIGHT", 8, 0)
    exportBtn:SetText("Export")
    exportBtn:SetScript("OnClick", ShowExportPopup)

    local clearBtn = CreateFrame("Button", nil, toolbar, "UIPanelButtonTemplate")
    clearBtn:SetSize(120, 22)
    clearBtn:SetPoint("LEFT", exportBtn, "RIGHT", 8, 0)
    clearBtn:SetText("Clear Auto Cache")
    clearBtn:SetScript("OnClick", function()
        if GeRODPS and GeRODPS.ClearAutoOverrideCache then
            GeRODPS.ClearAutoOverrideCache()
            Render()
        end
    end)

    statusFS = toolbar:CreateFontString(nil, "OVERLAY", "GameFontNormalSmall")
    statusFS:SetPoint("LEFT", clearBtn, "RIGHT", 16, 0)
    statusFS:SetText("")

    -- ── Header row ────────────────────────────────────────
    local header = CreateFrame("Frame", nil, inset)
    header:SetHeight(ROW_H)
    header:SetPoint("TOPLEFT", toolbar, "BOTTOMLEFT", 0, -4)
    header:SetPoint("TOPRIGHT", toolbar, "BOTTOMRIGHT", 0, -4)
    local hbg = header:CreateTexture(nil, "ARTWORK")
    hbg:SetAllPoints()
    hbg:SetColorTexture(0.15, 0.10, 0.05, 0.9)
    local hx = 0
    for c, def in ipairs(COL) do
        local fs = header:CreateFontString(nil, "OVERLAY", "GameFontNormalSmall")
        fs:SetPoint("LEFT", header, "LEFT", hx + 4, 0)
        fs:SetWidth(def.w - 8)
        fs:SetHeight(ROW_H)
        fs:SetJustifyH("LEFT")
        fs:SetJustifyV("MIDDLE")
        fs:SetText("|cFFFFD200" .. def.title .. "|r")
        hx = hx + def.w
    end

    -- ── ScrollFrame for the row list ──────────────────────
    scroll = CreateFrame("ScrollFrame", nil, inset, "UIPanelScrollFrameTemplate")
    scroll:SetPoint("TOPLEFT", header, "BOTTOMLEFT", 0, -4)
    scroll:SetPoint("BOTTOMRIGHT", inset, "BOTTOMRIGHT", -28, 8)

    content = CreateFrame("Frame", nil, scroll)
    content:SetSize(CONTENT_W, ROW_H)   -- height grows in Render
    scroll:SetScrollChild(content)

    -- Pre-create 64 rows (auto-grows in Render if more entries arrive)
    BuildRowPool(content, 64)

    -- Resize handle (bottom-right corner)
    local resize = CreateFrame("Button", nil, frame)
    resize:SetSize(16, 16)
    resize:SetPoint("BOTTOMRIGHT", -4, 4)
    resize:EnableMouse(true)
    resize:SetNormalTexture("Interface/ChatFrame/UI-ChatIM-SizeGrabber-Up")
    resize:SetHighlightTexture("Interface/ChatFrame/UI-ChatIM-SizeGrabber-Highlight")
    resize:SetPushedTexture("Interface/ChatFrame/UI-ChatIM-SizeGrabber-Down")
    resize:SetScript("OnMouseDown", function(_, button)
        if button == "LeftButton" then
            frame:StopMovingOrSizing()
            frame:StartSizing("BOTTOMRIGHT")
        end
    end)
    resize:SetScript("OnMouseUp", function(_, button)
        if button == "LeftButton" then
            frame:StopMovingOrSizing()
            SaveSize(frame)
        end
    end)

    table.insert(UISpecialFrames, FRAME_NAME)
    ApplySavedGeometry(frame)
    frame:Hide()
    return frame
end

-- ============================================================
-- Public API
-- ============================================================

function TOOL.ShowSpellOverrideListView()
    local f = CreateFrameOnce()
    if not f:IsShown() then
        ApplySavedGeometry(f)
        f:Show()
        Render()
    end
end

function TOOL.HideSpellOverrideListView()
    if frame and frame:IsShown() then frame:Hide() end
end

function TOOL.ToggleSpellOverrideListView()
    local f = CreateFrameOnce()
    if f:IsShown() then
        f:Hide()
    else
        ApplySavedGeometry(f)
        f:Show()
        Render()
    end
end

-- Register into the minimap menu (Core.lua provides RegisterTool)
if TOOL.RegisterTool then
    TOOL.RegisterTool("Spell Override Cache", TOOL.ToggleSpellOverrideListView)
end
