--[[
    GeRODPS_Tools / SpellOverrideListView.lua

    "Spell Override Cache" tool — view + export learned override pairs.

    Two data sources (queried via GeRODPS.GetAllOverridePairs):
      • manual: GeRODPS.SpellOverrideList (hard-coded by dev)
      • auto:   GeRODPS.dbGlobal.spellOverrideMap (runtime auto-learned)

    Display: sortable read-only table (class → spec → override).

    Export: opens a popup with a MultiLineEditBox containing the merged
    entries formatted as Lua source — sorted by class (WoW CLASS_SORT_ORDER),
    then by spec, then by overrideID. Dev copies the block into
    SpellOverrideList.lua to promote auto entries to hard-coded.

    Public:
        GeRODPS_Tools.ShowSpellOverrideListView()
        GeRODPS_Tools.HideSpellOverrideListView()
        GeRODPS_Tools.ToggleSpellOverrideListView()
]]

GeRODPS_Tools = GeRODPS_Tools or {}
local TOOL = GeRODPS_Tools

local FRAME_NAME    = "GeRODPS_ToolsSpellOverrideListViewFrame"
local SCREEN_MARGIN = 50
local ROW_H         = 18

local DEFAULT_W, DEFAULT_H = 880, 540
local MIN_W,     MIN_H     = 640, 360
local MAX_W,     MAX_H     = 1400, 900

-- Column widths sum to 800 (frame content area = 880 - 40 margins ≈ 840).
local COL = {
    { key="class",        title="Class",            w=110 },
    { key="spec",         title="Spec",             w=110 },
    { key="overrideName", title="Override Spell",   w=180 },
    { key="overrideID",   title="ID",               w=70  },
    { key="baseName",     title="Base Spell",       w=180 },
    { key="baseID",       title="ID",               w=70  },
    { key="source",       title="Source",           w=70  },
}
local CONTENT_W = 0
for _, c in ipairs(COL) do CONTENT_W = CONTENT_W + c.w end

-- WoW class sort order — fall back to alpha if global missing.
local CLASS_SORT_ORDER = CLASS_SORT_ORDER or {
    "DEATHKNIGHT", "DEMONHUNTER", "DRUID", "EVOKER", "HUNTER", "MAGE",
    "MONK", "PALADIN", "PRIEST", "ROGUE", "SHAMAN", "WARLOCK", "WARRIOR",
}
local _classRank = {}
for i, c in ipairs(CLASS_SORT_ORDER) do _classRank[c] = i end
local function ClassRank(c) return _classRank[c] or 99 end

-- ============================================================
-- DB (geometry only)
-- ============================================================

local function GetDB()
    GeRODPS_ToolsDB = GeRODPS_ToolsDB or {}
    GeRODPS_ToolsDB.spellOverrideView = GeRODPS_ToolsDB.spellOverrideView or {}
    return GeRODPS_ToolsDB.spellOverrideView
end

-- ============================================================
-- Sorted snapshot from GeRODPS.GetAllOverridePairs
-- Returns an array of entries: { overrideID, base, class, spec,
--                                overrideName, baseName, source }
-- Sorted by class rank → spec → overrideID.
-- ============================================================

local function GetSortedSnapshot()
    local entries = {}
    if not (GeRODPS and GeRODPS.GetAllOverridePairs) then return entries end
    local pairs_ = GeRODPS.GetAllOverridePairs()
    for ovID, e in pairs(pairs_) do
        entries[#entries + 1] = {
            overrideID   = ovID,
            base         = e.base,
            class        = e.class or "UNKNOWN",
            spec         = e.spec  or "Unknown",
            overrideName = e.overrideName or tostring(ovID),
            baseName     = e.baseName     or tostring(e.base),
            source       = e.source or "auto",
        }
    end
    table.sort(entries, function(a, b)
        local ra, rb = ClassRank(a.class), ClassRank(b.class)
        if ra ~= rb then return ra < rb end
        if a.spec ~= b.spec then return a.spec < b.spec end
        return a.overrideID < b.overrideID
    end)
    return entries
end

-- ============================================================
-- Export string builder
-- Produces a Lua source block — drop-in pasteable into
-- SpellOverrideList.lua. Format mirrors the existing entries
-- (class header comment, then aligned KV entries).
-- ============================================================

local function BuildExportText(entries)
    local lines = {}
    local function push(s) lines[#lines + 1] = s end

    push("    -- Generated by GeRODPS_Tools → Spell Override Cache → Export")
    push("    -- " .. date("%Y-%m-%d %H:%M:%S") .. "  |  total: " .. #entries)
    push("")

    local lastClass = nil
    for _, e in ipairs(entries) do
        if e.class ~= lastClass then
            if lastClass then push("") end
            push("    -- ============================================================")
            push("    -- " .. e.class)
            push("    -- ============================================================")
            push("")
            lastClass = e.class
        end
        -- Inline header comment for the pair
        push(string.format(
            "    -- %s | %s → %s",
            e.spec, e.overrideName, e.baseName))
        push(string.format(
            "    [%d] = { base = %d, class = %q, spec = %q,\n             overrideName = %q, baseName = %q },",
            e.overrideID, e.base, e.class, e.spec, e.overrideName, e.baseName))
    end

    return table.concat(lines, "\n")
end

-- ============================================================
-- State
